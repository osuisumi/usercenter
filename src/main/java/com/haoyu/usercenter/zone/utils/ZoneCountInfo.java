package com.haoyu.usercenter.zone.utils;

public class ZoneCountInfo {
	
	private int zoneDynamicNum;
	
	private int scheduleNum;
	
	private int diaryNum;
	
	private int resourceNum;
	
	private int galleryNum;
	
	private int fansNum;
	
	private int followNum;

	public int getZoneDynamicNum() {
		return zoneDynamicNum;
	}

	public void setZoneDynamicNum(int zoneDynamicNum) {
		this.zoneDynamicNum = zoneDynamicNum;
	}

	public int getScheduleNum() {
		return scheduleNum;
	}

	public void setScheduleNum(int scheduleNum) {
		this.scheduleNum = scheduleNum;
	}

	public int getDiaryNum() {
		return diaryNum;
	}

	public void setDiaryNum(int diaryNum) {
		this.diaryNum = diaryNum;
	}

	public int getResourceNum() {
		return resourceNum;
	}

	public void setResourceNum(int resourceNum) {
		this.resourceNum = resourceNum;
	}

	public int getGalleryNum() {
		return galleryNum;
	}

	public void setGalleryNum(int galleryNum) {
		this.galleryNum = galleryNum;
	}

	public int getFansNum() {
		return fansNum;
	}

	public void setFansNum(int fansNum) {
		this.fansNum = fansNum;
	}

	public int getFollowNum() {
		return followNum;
	}

	public void setFollowNum(int followNum) {
		this.followNum = followNum;
	}
	
	

}
